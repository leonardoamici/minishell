#include "libft.h"

int	ft_string(char *str)
{
	int	x;

	x = 0;
	if (str == 0)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (str[x] != '\0')
	{
		write(1, &str[x], 1);
		x++;
	}
	return (x);
}
